function isMeetingCalendarValidBruteForce(intervals) {
    // ── Шаг 1: Определяем вспомогательную функцию overlap ───────────────────
    //
    // Функция принимает два интервала и проверяет,
    // пересекаются ли они.
    //
    // Два интервала [a, b] и [c, d] пересекаются, если:
    //   • a >= c И a < d  →  начало первого находится ВНУТРИ второго
    //   • ИЛИ c >= a И c < b  →  начало второго находится ВНУТРИ первого
    //
    // Важно: граничные значения НЕ считаются пересечением:
    //   [1,5] и [5,10] → НЕ пересекаются (встречи идут встык)
    //   Проверка строгая: interval1[0] < interval2[1] (не <=)
    //
    const overlap = (interval1, interval2) => {
        return (
        // Случай 1: начало interval1 попадает в диапазон interval2
        // interval1[0] >= interval2[0] означает: interval1 начинается не раньше interval2
        // interval1[0] < interval2[1]  означает: interval1 начинается ДО конца interval2
        (interval1[0] >= interval2[0] && interval1[0] < interval2[1]) ||
            // Случай 2: начало interval2 попадает в диапазон interval1
            // Симметричная проверка для обратного порядка
            (interval2[0] >= interval1[0] && interval2[0] < interval1[1]));
    };
    // ── Шаг 2: Двойной цикл — перебираем все пары интервалов ────────────────
    //
    // Внешний цикл: i от 0 до n-1 (каждый интервал как "первый" в паре)
    // Внутренний цикл: j от i+1 до n-1 (каждый следующий интервал как "второй")
    //
    // Почему j = i + 1?
    //   Чтобы не сравнивать интервал сам с собой (i == j)
    //   и не проверять одну и ту же пару дважды (i,j) и (j,i).
    //
    for (let i = 0; i < intervals.length; i++) {
        for (let j = i + 1; j < intervals.length; j++) {
            // ── Шаг 3: Проверяем пересечение текущей пары ─────────────────────
            if (overlap(intervals[i], intervals[j])) {
                // Если пересечение найдено → календарь НЕ валиден
                return false;
            }
        }
    }
    // ── Шаг 4: Ни одного пересечения не найдено → calendar валиден ──────────
    return true;
}
