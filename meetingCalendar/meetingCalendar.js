//   3. Для каждой пары вызвать overlap. Если пересечение найдено — return false.
//   4. Если ни одной пересекающейся пары не найдено — return true.
//
// СЛОЖНОСТЬ:
//   Временная: O(n²) — перебираем все n*(n-1)/2 пар
//   Пространственная: O(1) — не используем доп. структур данных
