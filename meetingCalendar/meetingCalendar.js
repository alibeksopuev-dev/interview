//   Перебрать все возможные пары интервалов и проверить,
//   не пересекаются ли они между собой. Если хотя бы одна
//   пара пересекается — вернуть false.
//
// АЛГОРИТМ:
//   1. Определить вспомогательную функцию overlap(interval1, interval2),
