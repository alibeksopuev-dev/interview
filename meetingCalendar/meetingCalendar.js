// ПОДХОД 2: С СОРТИРОВКОЙ (Optimal / Sorting Approach)
// ============================================================
//
// ИДЕЯ:
//   После сортировки интервалов по времени начала, перекрывающиеся
//   интервалы гарантированно окажутся рядом друг с другом.
//   Значит, достаточно проверить только СОСЕДНИЕ пары — это
//   линейный проход вместо квадратичного.
//
// АЛГОРИТМ:
//   1. Отсортировать intervals по времени начала (возрастание).
//   2. Итерироваться по отсортированным интервалам.
//   3. Для каждого интервала i проверить:
//      intervals[i][1] > intervals[i+1][0]
//      (конец текущего > начало следующего → пересечение!)
//   4. Если пересечение найдено — return false.
//   5. Если цикл завершился без пересечений — return true.
//
// СЛОЖНОСТЬ:
//   Временная: O(n log n) — сортировка доминирует; линейный проход O(n)
//   Пространственная: O(1) — сортировка in-place, доп. памяти не нужно
// ============================================================
function isMeetingCalendarValid(intervals) {
    // ── Шаг 1: Сортировка по времени начала ─────────────────────────────────
    //
    // intervals.sort((a, b) => a[0] - b[0])
    //   a[0] — время начала интервала a
    //   b[0] — время начала интервала b
    //   a[0] - b[0] → возрастающий порядок (меньшее значение = раньше)
    //
    // Пример:
    //   До сортировки:  [[8,10],[1,3],[2,6],[15,18]]
    //   После:          [[1,3],[2,6],[8,10],[15,18]]
    //
    intervals.sort((a, b) => a[0] - b[0]);
    // ── Шаг 2: Линейный проход по отсортированным интервалам ────────────────
    //
    // Цикл идёт до length - 1, потому что мы сравниваем текущий (i)
    // со следующим (i+1). Последний элемент не имеет "следующего".
    //
    for (let i = 0; i < intervals.length - 1; i++) {
        // ── Шаг 3: Проверка пересечения соседних интервалов ───────────────────
        //
        // intervals[i][1]   — время КОНЦА текущей встречи
        // intervals[i+1][0] — время НАЧАЛА следующей встречи
        //
        // Если конец текущей встречи СТРОГО БОЛЬШЕ начала следующей →
        // встречи пересекаются!
        //
        // Граничный случай (встречи "встык"):
        //   intervals[i][1] == intervals[i+1][0] → НЕ пересечение (строгое >)
        //   Пример: [1,5] и [5,10] → 5 > 5 = false → ОК
        //
        if (intervals[i][1] > intervals[i + 1][0]) {
            // Найдено пересечение → calendar НЕ валиден
            return false;
        }
    }
    // ── Шаг 4: Пересечений нет → calendar валиден ───────────────────────────
    return true;
}
